# Take three inputs from the user and perform Sub, Mul and div

num_1= int(input("Enter the first number: "))
num_2= int(input("Enter the second number: "))
num_3= int(input("Enter the third number: "))

sub = num_1-num_2- num_3
mul = num_1*num_2*num_3
div = num_1%num_2%num_3

print("Subtraction of the given numbers is :", sub)
print("Multiplication of the given numbers is :", mul)
print("Division of the given numbers is :", div)